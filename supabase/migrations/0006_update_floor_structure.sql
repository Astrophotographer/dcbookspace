-- =====================================================
-- 0006: 건물별 층 구조 재정의
--   본당  : M(로비) · 1 · 2 · 3 · 4 · 5  (총 6개층)
--   교육관: B2 · B1 · 1 · 2 · 3 · 4 · 5 · 6  (총 8개층)
--
-- 기존 1F/2F floor는 라벨만 갱신해서 매달린 호실·신청 이력을 그대로 보존.
-- 본당 "식당" room 은 신청 이력 보존을 위해 M(로비)층으로 이전.
-- 그 다음 비어 있는 지하 floor 를 삭제한다.
-- 교육관 기존 호실(유치부실 등) 은 active=false 로 비활성화.
-- 전체가 한 트랜잭션이라 도중 실패 시 한 번에 롤백된다.
-- =====================================================

begin;

-- ───── 본당 (id: 11111111-…) ─────

-- 1) 본당 신규 floor 먼저 INSERT — 식당을 옮길 M(로비) 의 id 를 명시적으로 확보
insert into floors (id, building_id, label, display_order) values
  ('a1a1a1a1-0000-0000-0000-000000000004', '11111111-1111-1111-1111-111111111111', 'M(로비)', 1),
  ('a1a1a1a1-0000-0000-0000-000000000005', '11111111-1111-1111-1111-111111111111', '3층',     4),
  ('a1a1a1a1-0000-0000-0000-000000000006', '11111111-1111-1111-1111-111111111111', '4층',     5),
  ('a1a1a1a1-0000-0000-0000-000000000007', '11111111-1111-1111-1111-111111111111', '5층',     6);

-- 2) 기존 1F → "1층", 2F → "2층" (UUID 유지 → 매달린 호실·신청 이력 그대로)
update floors set label = '1층', display_order = 2
 where id = 'a1a1a1a1-0000-0000-0000-000000000001';

update floors set label = '2층', display_order = 3
 where id = 'a1a1a1a1-0000-0000-0000-000000000002';

-- 3) 지하의 "식당" room 을 M(로비)층으로 이전 — 이전 좌표는 새 도면에 맞게 초기화
update rooms
   set floor_id = 'a1a1a1a1-0000-0000-0000-000000000004',
       map_x = 30, map_y = 30, map_w = 40, map_h = 40
 where floor_id = 'a1a1a1a1-0000-0000-0000-000000000003';

-- 4) 이제 비어 있는 지하 floor 삭제
delete from floors
 where id = 'a1a1a1a1-0000-0000-0000-000000000003';

-- ───── 교육관 (id: 22222222-…) ─────

-- 5) 교육관 신규 floor INSERT
insert into floors (id, building_id, label, display_order) values
  ('b2b2b2b2-0000-0000-0000-000000000003', '22222222-2222-2222-2222-222222222222', 'B2',  1),
  ('b2b2b2b2-0000-0000-0000-000000000004', '22222222-2222-2222-2222-222222222222', 'B1',  2),
  ('b2b2b2b2-0000-0000-0000-000000000005', '22222222-2222-2222-2222-222222222222', '3층', 5),
  ('b2b2b2b2-0000-0000-0000-000000000006', '22222222-2222-2222-2222-222222222222', '4층', 6),
  ('b2b2b2b2-0000-0000-0000-000000000007', '22222222-2222-2222-2222-222222222222', '5층', 7),
  ('b2b2b2b2-0000-0000-0000-000000000008', '22222222-2222-2222-2222-222222222222', '6층', 8);

-- 6) 기존 1F → "1층", 2F → "2층"
update floors set label = '1층', display_order = 3
 where id = 'b2b2b2b2-0000-0000-0000-000000000001';

update floors set label = '2층', display_order = 4
 where id = 'b2b2b2b2-0000-0000-0000-000000000002';

-- 7) 교육관 기존 호실(유치부실·유년부실·청소년부실·청년부실) 비활성화
--    신청 이력 보존을 위해 삭제하지 않고 active=false 로만 표시
update rooms set active = false
 where floor_id in (
   'b2b2b2b2-0000-0000-0000-000000000001',
   'b2b2b2b2-0000-0000-0000-000000000002'
 );

commit;
