-- =====================================================
-- 0012: 부서 2뎁스 트리화
--
-- - departments 에 parent_id self-reference 추가
--   * NULL → 그룹(대분류)
--   * 값 있음 → 소분류(leaf)
-- - 결재 라인(dept_head_id / elder_id), reservations.dept_id 는 leaf 만 사용 (정책)
-- - 기존 시드 부서 5개 (교육부/찬양부/청년부/선교부/관리부) 정리
-- - 등촌교회 실 조직 시드 (그룹 9 + leaf 약 62)
-- =====================================================

alter table departments
  add column if not exists parent_id uuid references departments(id) on delete restrict;

create index if not exists idx_departments_parent on departments(parent_id);

-- 기존 시드 부서 정리. reservations.dept_id 는 on delete set null 이라 자동 처리됨.
delete from departments
 where parent_id is null
   and name in ('교육부', '찬양부', '청년부', '선교부', '관리부');

